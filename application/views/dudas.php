            <h2>¿Qué es la trata laboral?</h2>
            <p>Una forma de trata de personas. Es la  contratación, el ocultamiento, el transporte, la provisión o la obtención de una persona para trabajo o servicios, por medio del uso de fuerza, fraude o coerción con el fin de someterla a esclavitud involuntaria, peonaje, servidumbre por deudas o esclavitud.</p>

            <p>&nbsp;</p>
            <p>&nbsp;</p>
            <h2>Y trabajo forzado... ¿Qué es?</h2>
            <p>El trabajo forzado es todo trabajo o servicio que una persona está obligada a hacer en contra de su voluntad, sin que le paguen, o con amenazas. Es una forma de trata laboral.</p>
            <p>&nbsp;</p>
