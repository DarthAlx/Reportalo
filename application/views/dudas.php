            <h2>¿Tengo derechos?</h2>
            <div class="row cuadro text-right">
              <div class="pull-right" style="background: #00cc99;"><p>Tú puedes trabajar en donde quieras y cuando quieras siempre y cuando tu trabajo no sea ilegal.</p></div>
            </div>

            <div class="row cuadro text-right">
              <div class="pull-right" style="background: #009966;"><p>Nadie puede obligarte a prestar tus servicios a un patrón si no te pagan o si no estás de acuerdo.</p></div>
